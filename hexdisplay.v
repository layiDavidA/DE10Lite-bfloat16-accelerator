4'd5: Y = 8'b10010010;
4'd6: Y = 8'b10000010;
4'd7: Y = 8'b11111000;
4'd8: Y = 8'b10000000;
4'd9: Y = 8'b10010000;
4'd10: Y = 8'b10001000;
4'd11: Y = 8'b10000011;
4'd12: Y = 8'b10100111;
4'd13: Y = 8'b10100001;
4'd14: Y = 8'b10000110;
4'd15: Y = 8'b10001110;
default: Y =  8'b10111111;
endcase
end
endmodule

